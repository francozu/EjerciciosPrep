function divide(x, y) {
  // La función recibe dos argumentos llamados "x" e "y" que son números.
  // Retorna el resultado de la division de estos argumentos.
  // Por ejemplo: 
  // 10, 5 ---> 2
  // 5, 5 ---> 1
  // Tu código:
  return x / y;
}
console.log(divide(4,6));
console.log(divide(20,6));
console.log(divide(40,6));
console.log(divide(36,6));

