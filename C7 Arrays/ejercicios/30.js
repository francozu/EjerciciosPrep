function encontrarElementoRepetido(numeros) {
  // La función recibe un argumento "numeros" que es un array de números.
  // Retorna el primer elemento repetido que se encuentre en el array.
  // Tu código:
  // Crear un conjunto para hacer un seguimiento de los números que ya hemos visto
  // Crear un conjunto para hacer un seguimiento de los números que ya hemos visto
  var numerosVistos = new Set();

  // Recorrer el arreglo de números
  for (var i = 0; i < numeros.length; i++) {
    var numero = numeros[i];
    
    // Comprobar si el número ya está en el conjunto
    if (numerosVistos.has(numero)) {
      // Si el número ya está en el conjunto, es el primer número repetido
      return numero;
    }
    
    // Si el número no está en el conjunto, añadirlo
    numerosVistos.add(numero);
  }

  // Si no se encuentra ningún número repetido, retornar null
  return null;

}
